int	str_nbr(char *s)
{
	int		n;

	n = 0;
	while ('0' <= *s && *s <= '9')
		n = 10 * n + (*(s++) - '0');
	return (n);
}

int	verif(char *argv, int len)
{
	if (argv && '0' <= *argv && *argv <= '9')
		return (len < 8 && (!*(argv + 1) || verif(argv + 1, len + 1)));
	return (0);
}
