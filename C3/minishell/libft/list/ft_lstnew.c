#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*n;

	n = malloc(sizeof(t_list));
	if (!n)
		return (NULL);
	if (content)
		n -> content = content;
	else
		n -> content = NULL;
	n -> next = NULL;
	return (n);
}
