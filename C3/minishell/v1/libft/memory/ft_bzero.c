#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;

	ptr = s;
	while (n--)
		*ptr++ = 0;
}
/*
Stp1: conversion to char, 1char = 1byte(1 octet)
Stp2: bytes to zero
*/
